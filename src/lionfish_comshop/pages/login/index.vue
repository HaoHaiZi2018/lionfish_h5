      handler: function(route) {
        this.redirect = route.query && route.query.redirect
      },
      immediate: true
    }
  },
  methods: {
	  click_btn(){
		this.isShow=false,
		this.isShow2=true
	  },
	  click_btn2(){
		  this.isShow3=false,
		  this.isShow4=true
	  },
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    handleLogin() {
      this.$http({
        controller : 'user.weblogin_do',
        i: 3,
        user: this.loginForm.username,
        password: this.loginForm.password
      }).then(response => {
        console.log(response)
        var result = response;
        if(result != null && result.member_id != -1){
          alert('{{$t(common.dengluchenggong)}}');
          this.$wx.setStorageSync('token', response.token)
          setToken(response.token);
          this.$router.push({path: '/lionfish_comshop/pages/index/index'});
        }else{
          alert("{{$t(common.denglushibai)}}");
        }
      })
    },
    gotoRegist: function(){
      this.$router.push({path: '/regist'});
    },
    hideTopAndFooter: function(){
      this.$wx.setNavigationBarTitle({
        title: 'Login',
        showLogo:false,
        showMore:false,
        showBack:true
      })
    }
  },
  created: function(){
    this.hideTopAndFooter();
  }
}
